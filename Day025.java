package Day025;

public class main {
    public static void main(String[] args) {
        int a, b, c, d;
        
        a = 2;
        b = 3;
        c = 4;
        d = 5;       
        
        System.out.println(a++); // post increment
        System.out.println(++b); // pre increment
        System.out.println(c--); // post decrement
        System.out.println(--d); // pre decrement
        
    }
    
}
